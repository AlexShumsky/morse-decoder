const MORSE_TABLE = {
    '': ' ',
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let morseArr = expr.split('').map((el, i) => {
        return ((i + 1) % 10 == 0 && i != 0) ? el + ' ' : el;
    }).join('').split(' ').map(el => el.replace(/../g, function (el) {
        return (el == 10) ? '.' :
            (el == 11) ? '-' : '';
    }))
    return morseArr.map(el => MORSE_TABLE[el]).join('').trim();
}
module.exports = {
    decode
}